package com.projectswg.holocore.resources.gameplay.world.map;

/**
 * Created by Waverunner on 6/1/2015
 */
public class MappingTemplate {
	
	private String template;
	private String name;
	private String category;
	private String subcategory;
	private int type;
	private int flag;

	public MappingTemplate() {
		template = null;
		name = null;
		category = null;
		subcategory = null;
		type = 0;
		flag = 0;
	}

	public String getTemplate() {
		return template;
	}

	public void setTemplate(String template) {
		this.template = template;
	}

	public String getName() {
		return (name.isEmpty() ? getStfName() : name);
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getSubcategory() {
		return subcategory;
	}

	public void setSubcategory(String subcategory) {
		this.subcategory = subcategory;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

	public String getStfName() {
		return "@map_loc_cat_n:" + (getSubcategory().isEmpty() ? getCategory() : getSubcategory());
	}
}
